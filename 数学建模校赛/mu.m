function U=mu(Ts,T_inf)
M=0.018;
a=4.785/2.669;
T=(2*Ts+T_inf)/3;
Pc=22.064*1000000;
Tc=647.096;
w=0.3443;
sigma=(2.36545-0.0738*w)/((Pc/Tc)^(1/3));
x=(0.7915+0.1693*w)*Tc;
T_s=T/x;
A=1.16145;
B=0.14874;
C=0.52487;
D=0.77320;
E=2.16178;
F=2.43787;
Omega=A/((T_s)^B)+C/exp(D*T_s)+E/exp(F*T_s);
U=2.669*((M*T)^(1/2)) / ((sigma^2)*Omega*100000000)*a;
end