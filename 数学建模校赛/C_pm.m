function C=C_pm(Ts,T_inf)%Ts是t时刻的液体表面温度，T_inf是环境温度
%网上查找的温度范围在[5°c,60°c]的恒容比热容
t=[5,10,15,20,25,30,35,40,455,50,55,60];
t=t+273.15;
C_1=[1.8894,1.8947,1.9002,1.9059,1.9119,1.918,1.9245,1.9314,1.9388,1.9468,1.9554,1.9648];
C_1=C_1*1000;
%
p_1=polyfit(t,C_1,3);%三次多项式拟合
T=(2*Ts+T_inf)/3;
C=p_1(4)+p_1(3)*T+p_1(2)*T^2+p_1(1)*T^3;%水蒸气的恒压比热容(J/(kg.K))
end