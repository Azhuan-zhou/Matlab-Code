function C=C_vm(Ts,T_inf)%Ts是t时刻的液体表面温度，T_inf是环境温度
%网上查找温度范围在[5°c,60°c]的恒容比热容
t=[5,10,15,20,25,30,35,40,45,50,55,60];
t=t+273.15;
C_2=[1.4226,1.4269,1.4314,1.4359,1.4405,1.4452,1.4501,1.4552,1.4606,1.4663,1.4724,1.4789];
C_2=C_2*1000;
%
p_2=polyfit(t,C_2,3);%三次多项式拟合
T=(2*Ts+T_inf)/3;
C=p_2(4)+p_2(3)*T+p_2(2)*T^2+p_2(1)*T^3;%水蒸气的恒容比热容(J/(kg.K))
end