clc;clear
x=0:1/100:1;
figure(1);
subplot(2,2,1);
y_1=exp(x);
plot(x,y_1,'r');
xlabel('x');
ylabel('y1');
legend('y_1=e^x');
grid on;
subplot(2,2,2);
y_2=ones(1,size(x,2))+x;
plot(x,y_2,'r');
xlabel('x');
ylabel('y2');
legend('y_2=1+x');
grid on;
subplot(2,2,3);
y_3=ones(1,size(x,2))+x+x.^2/2;
plot(x,y_3,'r');
xlabel('x');
ylabel('y3');
legend('y_3=1+x+(1/2)x^2');
grid on;
subplot(2,2,4);
y_4=ones(1,size(x,2))+x+x.^2/2+x.^3/6;
plot(x,y_3,'r');
xlabel('x');
ylabel('y4');
legend('y_4=1+x+(1/2)x^2+(1/6)x^3');
grid on;
figure(2);
plot(x,y_1,'k',x,y_2,'b',x,y_3,'g',x,y_4,'r');
xlabel('x');
ylabel('y');
legend('y_1=e^x','y_2=1+x','y_3=1+x+(1/2)x^2','y_4=1+x+(1/2)x^2+(1/6)x^3');
grid on;
