clc;clear
%输入信号为u（n）
B=[1,0,-1];B=1/100.49*B;A=[1,-1.8237,0.9801];%差分方程系数
xn_1=ones(1,150);%单位阶跃序列输入序列
yn_2=filter(B,A,xn_1);%单位阶跃响应
n=0:length(xn_1)-1;
figure(1)
subplot(1,2,1);%分图一画出单位阶跃响应
stem(n,yn_2,".");
xlabel("n");
ylabel("y(n)");
title("输入信号为u(n)时的输出波形");
%输入信号X(n)=sin(0.014n)+sin(0.4n)
xn_2=sin(0.014*n)+sin(0.4*n);%X(n)输入序列
yn_2=filter(B,A,xn_2);%X(n)=sin(0.014n)+sin(0.4n)的响应
figure(1)
subplot(1,2,2);%画出系统响应
stem(n,yn_2,".");
xlabel("n");
ylabel("y(n)");
title("输入信号为x(n)=sin(0.014n)+sin(0.4n)时的输出波形");