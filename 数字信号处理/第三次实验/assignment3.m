clc;clear
Fs=64;%采样频率64HZ
T=1/64;
n1=0:15;
n2=0:31;
n3=0:63;
%16点FFT
x16T=cos(8*pi*n1*T)+cos(16*pi*n1*T)+cos(20*pi*n1*T);
x6K16=fft(x16T,16);
x6k16=fftshift(x6K16);
%32点FFT
x32T=cos(8*pi*n2*T)+cos(16*pi*n2*T)+cos(20*pi*n2*T);
x6K32=fft(x32T,32);
x6k32=fftshift(x6K32);
%64点FFT
x64T=cos(8*pi*n3*T)+cos(16*pi*n3*T)+cos(20*pi*n3*T);
x6K64=fft(x64T,64);
x6k64=fftshift(x6K64);
%绘制图像
N1=-32:4:28;
N2=-32:2:30;
N3=-32:1:31;
figure(1)
subplot(3,1,1);%16点
stem(N1,abs(x6k16),"k.","markersize",10);
xlabel({"f/Hz";'图6(a)16点DFT[x_6(n)]'});
ylabel("幅度");
axis([-35,30,0,15]);
subplot(3,1,2);
stem(N2,abs(x6k32),"k.","markersize",10);
xlabel({"f/Hz";'图6(b)32点DFT[x_6(n)]'});
ylabel("幅度");
axis([-35,30,0,20]);
subplot(3,1,3);
stem(N3,abs(x6k64),"k.","markersize",10);
xlabel({"f/Hz";'图6(c)64点DFT[x_6(n)]'});
ylabel("幅度");
axis([-35,30,0,40]);